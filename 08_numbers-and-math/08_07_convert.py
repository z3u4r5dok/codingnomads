# Demonstrate how to:
# -------------------
# 1) Convert an int to a float
print(float(1))

# 2) Convert a float to an int
print(int(3.8))

# 3) Perform division using a float and an int.
print(4/3.9)

# 4) Use two variables to perform a multiplication.
mult_one = 35
mult_two = 94

mult_total = mult_one * mult_two

print(mult_total)


